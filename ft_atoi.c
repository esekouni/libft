#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		j;
	int		v;
	char	*ch;

	i = 0;
	ch = (char *)str;
	j = 1;
	v = 0;
	while ((ch[i] >= 9 && ch[i] <= 13) || ch[i] == 32)
		i++;
	if (ch[i] == '-' || ch[i] == '+')
	{
		if (ch[i] == '-')
			j *= -1;
		i++;
	}
	while (ch[i] && ch[i] >= '0' && ch[i] <= '9')
	{
		v *= 10;
		v += ch[i++] - '0';
	}
	return (v * j);
}
