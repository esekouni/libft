#include "libft.h"

void	*ft_memset(void *b, int n, size_t l)
{
	int		i;
	char	*c;

	c = (char *)b;
	i = 0;
	while (l > 0)
	{
		c[i] = (char)n;
		i++;
		l--;
	}
	return (b);
}
